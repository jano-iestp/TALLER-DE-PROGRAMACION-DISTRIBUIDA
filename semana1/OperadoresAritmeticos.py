# Operadores Aritméticos
a =5
b=4

suma = a+b
print("La suma es: ", suma)

resta = a+b
print("La resta es: ", resta)

multi = a*b
print("La multiplicación es: ", multi)

divi = a/b
print("La división de a/b es: ", divi)

modulo = a//b
print("La parte entera de la división de a/b es: ", modulo)

residuo = a%b
print("El residuo de la división de a/b es: ", residuo)

potencia = a**b
print("El potencia de a con exponente b: ", potencia)


